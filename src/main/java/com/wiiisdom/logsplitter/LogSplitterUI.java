    private void enableExecuteButton() {
        btnExecute.setEnabled(hasInput && hasOutput);
    }

    private void browseSource() {
        File path = new File(preferences.get("InputDir", System.getProperty("user.dir")));
        inputChooser.setCurrentDirectory(path);
        int returnVal = inputChooser.showOpenDialog(pnlBase);
        if (returnVal == JFileChooser.APPROVE_OPTION) {
            File file = inputChooser.getSelectedFile();
            Helper.setInputFile(file);
            txtSourceLog.setText(file.getAbsolutePath());
            hasInput = true;
            enableExecuteButton();
            preferences.put("InputDir", file.getParent());
        }
        btnOutFolder.requestFocusInWindow();
    }

    private void browseOutput() {
        File path = new File(preferences.get("OutputDir", System.getProperty("user.dir")));
        outputChooser.setCurrentDirectory(path);
        int returnVal = outputChooser.showSaveDialog(pnlBase);
        if (returnVal == JFileChooser.APPROVE_OPTION) {
            File file = outputChooser.getSelectedFile();
            String fileName = file.getAbsolutePath();
            if (!fileName.substring(fileName.length() - 5).equalsIgnoreCase(".XLSX")) {
                fileName = fileName.concat(".XLSX");
                file = new File(fileName);
            }
            Helper.setOutputFile(file);
            txtOutFolder.setText(fileName);
            hasOutput = true;
            enableExecuteButton();
            preferences.put("OutputDir", file.getParent());
        }
        btnExecute.requestFocusInWindow();
    }

    private void execute() {
        try {
            Helper.RunFile();
        } catch (IOException ex) {
            Logger.getLogger(LogSplitterUI.class.getName()).log(Level.SEVERE, null, ex);
        }
        JOptionPane.showMessageDialog(pnlBase,
                "Extraction complete.\nFile " + txtOutFolder.getText() + " is ready",
                "Process Complete",
                JOptionPane.INFORMATION_MESSAGE);
        System.exit(0);
    }

    private void CreateChoosers() {
        inputChooser = new JFileChooser();
        inputChooser.addChoosableFileFilter(newFilter("Log files", "log"));
        inputChooser.addChoosableFileFilter(newFilter("Text files", "txt"));
        inputChooser.setAcceptAllFileFilterUsed(false);

        outputChooser = new JFileChooser();
        outputChooser.addChoosableFileFilter(newFilter("XLSX files", "xlsx"));
        outputChooser.setAcceptAllFileFilterUsed(false);
    }

    private FileNameExtensionFilter newFilter(String description, String... extensions) {
        return new FileNameExtensionFilter(description, extensions);
    }

